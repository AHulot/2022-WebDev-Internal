			<h3>1. Email Phishing Scams</h3>
			
			<p>
				When scammers target thousands of people at the same time and send emails to them in an attempt to get personal information. These scammers will pretend to be 
				from some kind of organisation with something for the target to claim by providing some personal details. Alternatively, some scammers pretend to be working for 
				the government and might try to scare targets into giving information or money through threats.
			</p>
			
			<h3>2. Government Grant Scams</h3>

			<p>
				Scammers pretend to be from the government and contact people at random. They can pretend to be contacting the target on behalf of a political party or a
				government official and supply false information. They often claim that the target has been chosen to receive a reward or grant and will ask for personal
				information in order to claim it.
			</p>

			<h3>3. Fake Invoice Scams</h3>

			<p>
				When a scammer sends an invoice for products or services that the recipient did not request, or for a service or good that is completely fake. An email might be
				sent that appears to be from a legitimate business, stating that the target has ordered the items or service and must pay for it.
			</p>

			<h3>4. Romance Scams</h3>

			<p>
				To defraud someone of money, a scammer pretends to be in a relationship with them online. Typically, these con artists pose as someone they are not, using
				images and identities obtained from the internet. Email, social media, dating websites, and other websites and applications are used to develop these
				relationships.
			</p>

			<h3>5. Blackmail/Extortion</h3>

			<p>
				Extortion is a typical technique used by cyber criminals to gain money. This involves keeping someone hostage based on compromising material or information they
				have on them and forcing them to pay money to be released from the extortion attempt. They threaten to reveal the compromising material to family members,
				coworkers, and others unless money is put into the scam artists' account.
			</p>
			
